string BS2EventStringUtil::getSubEventString(uint16_t mainEvent, uint16_t subEvent)
{
	if (subEvent == 0)
		return "";

	switch (mainEvent)
	{
	case BS2_EVENT_VERIFY_SUCCESS:
	case BS2_EVENT_VERIFY_DURESS:
		return getSubEventStringVerify(subEvent);
	
	case BS2_EVENT_VERIFY_FAIL:
	case BS2_EVENT_IDENTIFY_FAIL:
		return getSubEventStringVerifyFail(subEvent);

	case BS2_EVENT_IDENTIFY_SUCCESS:
	case BS2_EVENT_IDENTIFY_DURESS:
		return getSubEventStringIdentify(subEvent);

	case BS2_EVENT_DUAL_AUTH_FAIL:
		return getSubEventStringDualAuthFail(subEvent);

	case BS2_EVENT_AUTH_FAILED:
		return getSubEventStringAuthFail(subEvent);

	case BS2_EVENT_ACCESS_DENIED:
		return getSubEventStringAccessDenied(subEvent);

	case BS2_EVENT_BYPASS_FAIL:
		return getSubEventStringBypassFail(subEvent);

	case BS2_EVENT_ABNORMAL_TEMPERATURE_DETECTED:
		return getSubEventStringAbnormalTemperature(subEvent);

	case BS2_EVENT_UNMASKED_FACE_DETECTED:
		return getSubEventStringUnmaskedFace(subEvent);

	case BS2_EVENT_USER_ENROLL_SUCCESS:
	case BS2_EVENT_USER_UPDATE_SUCCESS:
	case BS2_EVENT_USER_DELETE_SUCCESS:
	case BS2_EVENT_USER_DELETE_ALL_SUCCESS:
		return getSubEventStringUserEnroll(subEvent);

	case BS2_EVENT_USER_ENROLL_FAIL:
		return getSubEventStringUserEnrollFail(subEvent);
	
	case BS2_EVENT_USER_UPDATE_FAIL:
		return getSubEventStringUserUpdateFail(subEvent);

	case BS2_EVENT_DOOR_RELEASE:
		return getSubEventStringDoor(subEvent);
		
	case BS2_EVENT_FLOOR_RELEASE:
	case BS2_EVENT_FLOOR_ACTIVATE:
	case BS2_EVENT_FLOOR_DEACTIVATE:
		return getSubEventStringFloor(subEvent);

	case BS2_EVENT_ZONE_APB_VIOLATION:
	case BS2_EVENT_ZONE_TIMED_APB_VIOLATION:
		return getSubEventStringAPBZone(subEvent);

	case BS2_EVENT_DEVICE_ZONE_ENTRANCE_LIMIT_VIOLATION:
		return getSubEventStringEntranceLimitZone(subEvent);

	case BS2_EVENT_ZONE_INTERLOCK_VIOLATION:
		return getSubEventStringInterlockZone(subEvent);

	case BS2_EVENT_ZONE_AUTH_LIMIT_VIOLATION:
		return getSubEventStringAuthLimitZone(subEvent);

	//case BS2_EVENT_DEVICE_IO_DETECTED:
	//	return getSubEventStringDeviceIO(subEvent);

	default:
		break;
	}

	char buf[32];
	sprintf(buf, "Sub:0x%02X", subEvent);
	return string(buf);
}

