string BS2EventStringUtil::getEventString(uint16_t eventCode)
{
	uint16_t mainEvent = eventCode & BS2_EVENT_MASK;
	uint16_t subEvent = eventCode & BS2_SUB_EVENT_MASK;

	string mainStr = getMainEventString(mainEvent);
	string subStr = getSubEventString(mainEvent, subEvent);

	//if (0 < mainEvent && 0 < subEvent)
	//{
	//	return getComplexEventString(eventCode);
	//}

	return (subStr.empty()) ? mainStr :  mainStr + " (" + subStr + ")";
}

